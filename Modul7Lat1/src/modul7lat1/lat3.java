public class lat3 {
   public static void main (String[]args){
        for (int a=1;a<=3;a++){
            for (int i=1;i<=a;i++){
               System.out.print("*\t");
            }
            System.out.println();                                                                       
        }
        for (int a=2;a>=1;a--){
            for (int i=a;i>=1;i--){
                System.out.print("*\t");
            }
            System.out.println();
        }
        
    }
}

